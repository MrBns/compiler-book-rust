#[cfg(test)]
mod tests {
    use super::*;
    use crate::lexer::tokenize;
    use crate::parser::Parser;

    /// Helper: lex + parse + generate in one step.
    fn compile(src: &str) -> String {
        let tokens = tokenize(src);
        let mut parser = Parser::new(tokens);
        let program = parser.parse();
        generate(&program)
    }

    #[test]
    fn test_let_becomes_const() {
        let ts = compile("let x = 42;");
        assert!(
            ts.contains("const x = 42;"),
            "expected 'const x = 42;' in:\n{}",
            ts
        );
    }

    #[test]
    fn test_print_becomes_console_log() {
        let ts = compile("let n = 5; print(n);");
        assert!(
            ts.contains("console.log(n);"),
            "expected 'console.log(n);' in:\n{}",
            ts
        );
    }

    #[test]
    fn test_function_signature() {
        let ts = compile("fn add(a, b) { return a + b; }");
        assert!(
            ts.contains("function add(a: any, b: any): any {"),
            "expected function signature in:\n{}",
            ts
        );
    }

    #[test]
    fn test_if_else_structure() {
        let ts = compile("let x = 1; if x > 0 { print(x); } else { print(x); }");
        // condition is wrapped in parentheses
        assert!(ts.contains("if ((x > 0))"), "expected if condition in:\n{}", ts);
        assert!(ts.contains("} else {"), "expected else in:\n{}", ts);
    }

    #[test]
    fn test_equality_operator_becomes_strict() {
        // Pico '==' must become TypeScript '==='
        let ts = compile("let x = 1; let y = 1; if x == y { print(x); }");
        assert!(
            ts.contains("==="),
            "expected strict equality '===' in:\n{}",
            ts
        );
        // And '!=' must become '!=='
        let ts2 = compile("let a = 1; let b = 2; if a != b { print(a); }");
        assert!(
            ts2.contains("!=="),
            "expected strict not-equal '!==' in:\n{}",
            ts2
        );
    }

    #[test]
    fn test_string_literal_output() {
        let ts = compile(r#"let s = "hello";"#);
        assert!(
            ts.contains(r#"const s = "hello";"#),
            "expected string literal in:\n{}",
            ts
        );
    }

    #[test]
    fn test_number_strips_dot_zero() {
        // Whole numbers should NOT have ".0" in the output
        let ts = compile("let n = 100;");
        assert!(
            ts.contains("const n = 100;"),
            "expected '100' (not '100.0') in:\n{}",
            ts
        );
    }

    #[test]
    fn test_float_number_kept() {
        let ts = compile("let pi = 3.14;");
        assert!(
            ts.contains("const pi = 3.14;"),
            "expected '3.14' in:\n{}",
            ts
        );
    }

    #[test]
    fn test_full_program() {
        let src = r#"
fn greet(name) {
    print(name);
}
let x = 10;
let y = 20;
let sum = x + y;
if sum > 25 {
    greet("Big number!");
} else {
    greet("Small number.");
}
"#;
        let ts = compile(src);
        // Check key pieces of the output
        assert!(ts.contains("function greet(name: any): any {"));
        assert!(ts.contains("const x = 10;"));
        assert!(ts.contains("const y = 20;"));
        assert!(ts.contains("const sum = (x + y);"));
        assert!(ts.contains("if ((sum > 25))"));
    }
}
